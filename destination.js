export function renderData(data) {
    const renderAllDestinationData = data.slice(0, 4).map(function (item) {
        return `        
            <div class="destination-img" data-id="${item.id}"> 
            <img src="${item.imageUrl}">
             <p>${item.name}</p>              
            </div>        
         
        `;
    }).join("");

    document.getElementById('app').innerHTML += `<div class="destination-title" id="destination-title"><h1>Top Destination For Your Next Vacation</h1></div>
                                <div class="destination-container">${renderAllDestinationData}  </div>                                                                 
                                    <div class="button-container"><button id="see-all-button">See All Destination</button></div>
                                `;


    //add add event listener to see all destination button
    document.getElementById('see-all-button').addEventListener('click', function () {
        var seeAllButton = document.getElementById('see-all-button');
        var destinationContainer = document.querySelector('.destination-container');
        var remainingData = data.slice(4).map(function (item) {
            return `
        <div class="destination-img remaining" data-id="${item.id}">
        <img src="${item.imageUrl}">
          <p>${item.name}</p>
        </div> 
        
        `;
        }).join("");
        if (seeAllButton.innerText === "See All Destination") {
            destinationContainer.innerHTML += remainingData;
            seeAllButton.innerText = "Hide"
        } else {
            var remainingImages = document.querySelectorAll('.destination-img.remaining'); 
            remainingImages.forEach(function (image) {
                image.remove()
            })
            seeAllButton.innerText = "See All Destination"
        }
    });
    
     // Add click event listener to each destination image
    document.querySelectorAll('.destination-img').forEach(function (image) {
    image.addEventListener('click', function () {
        const destinationId = Number(image.getAttribute('data-id'));
        showDestinationDetails(destinationId);        
        document.getElementById('activities').remove()
        document.getElementById('footer').remove() 
           

    });
});

    
} 

// Function to show details of the selected destination
function showDestinationDetails(id) {
    const destination = allDestinationData.find(function(item) {
        return item.id === id;
    });

    if (destination) {
        const detailHTML = `
            <div class="destination-details">
                <h2>${destination.name}</h2>
                <img src="${destination.imageUrl}">
                <p>${destination.description}
                <a href="${destination.locationUrl}" class="destination-link" target="_blank" >View More about ${destination.name}</a></p>
            </div>
        `;
        document.getElementById('app').innerHTML = detailHTML;
        

    }
}
